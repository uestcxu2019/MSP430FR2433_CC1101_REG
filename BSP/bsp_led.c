#include "bsp_led.h"


/************************************************************************************************
 *  说	明 : GPIO引脚低功耗初始化
 *  参	数 : 无
 *  返回值  : 无
*************************************************************************************************/
void GPIO_LP_Init(void)
{
	//初始化,配置为输出方向
	P1DIR = 0xFF;
	PADIR = 0xFFFF;
	P2DIR = 0xFF;
	PBDIR = 0xFFFF;
	P3DIR = 0xFF;

	//将所有的引脚初始化配置为输出低电平
	P1OUT = 0x00;
	PAOUT = 0x0000;
	P2OUT = 0x00;
	PBOUT = 0x0000;
	P3OUT = 0x00;
}


/************************************************************************************************
 *  说	明 : LED相关引脚初始化
 *  参	数 : 无
 *  返回值  : 无
*************************************************************************************************/
void LED_Config(void)
{
	P1DIR |=  BIT0;
	P1OUT &=~ BIT0;		//LED灯初始化配置为输出低电平(P1.0)

	P1DIR &=~ BIT1;		//按键1为输入方向(P1.1)
	P1REN |=  BIT1;
	P1OUT |=  BIT1;		//上拉使能
}



